// Is performing module install/update?
$isModuleAction = @(// action module "system"
        !empty($GLOBALS['xoopsModule']) && 'system' === $GLOBALS['xoopsModule']->getVar('dirname', 'n') && // current dirname
        ($modversion['dirname'] == $_POST['dirname'] || $modversion['dirname'] == $_POST['module']) && // current op
        ('update_ok' === $_POST['op'] || 'install_ok' === $_POST['op'] || 'uninstall_ok' === $_POST['op']) && // current action
        'modulesadmin' === $_POST['fct']);
if ($isModuleAction) {
    include_once(XOOPS_ROOT_PATH . '/class/xoopslists.php');
    $files = XoopsLists::getHtmlListAsArray(__DIR__ . '/templates/');
    $list = array();
    foreach ($files as $file => $name) {
        // The valid file name must be: archivio_.tpl
        if (preg_match("/^{$modversion['dirname']}_(.*)?\.(tpl)$/i", $name, $matches)) {
            if (empty($matches[1]))
                continue;
            $list[] = array('file' => $name, 'type' => 'default', 'description' => $matches[1]);
        }
    }
    $modversion['templates'] = $list;
}



